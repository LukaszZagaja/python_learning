import math

#help(math)
value = 4.35

print("\n")
print("Value of our variable and then rounding with math module")
print(value)
print(math.floor(value))
print(math.ceil(value))
print("\n")

print("Python rounding")
print(round(value))
print(round(4.5))
print(round(5.5))
print("\n")

print("Math: pi, e, inf, nan")
print(math.pi)
print(math.e)
print(math.inf)
print(math.nan)
print("\n")

print("Math: log, sin, radians to degrees")
print(math.log(math.e))
print(math.sin(10))
print(math.degrees(math.pi/2))
print(math.radians(180))
